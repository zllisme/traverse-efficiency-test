public class Demo {
    private int index;
    private String str;

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public String getStr() {
        return str;
    }

    public void setStr(String str) {
        this.str = str;
    }

    public Demo(int index, String str) {
        this.index = index;
        this.str = str;
    }
}
